"""
Protocol-compatible mock server for the current LTX2 streaming UI.

This mock speaks the same websocket API shape as `server/main.py` for:
- `session_init_v2`
- `append_prompt`
- `simple_generate`
- prompt window reset / rewrite control messages

It streams a small fragmented MP4 clip generated by ffmpeg so the frontend can
exercise the real AV pipeline without needing a GPU backend.
"""

