LATENCY_MS = 200
SESSION_TIMEOUT_SECONDS = 300
MOCK_FRAME_WIDTH = 640
MOCK_FRAME_HEIGHT = 352
MOCK_FPS = 24
MOCK_DURATION_SECONDS = 5.0
MOCK_STREAM_CHUNK_SIZE_BYTES = 256 * 1024
MOCK_STREAM_CHUNK_DELAY_MS = 15
MOCK_AV_MIME = 'video/mp4; codecs="avc1.42E01E"'
FFMPEG_BIN = shutil.which(os.getenv("FASTVIDEO_FFMPEG_BIN", "ffmpeg"))
MOCK_SEGMENT_BYTES: bytes | None = None


class _HeartbeatAccessLogFilter(logging.Filter):
    """Drop noisy access logs for frequent health/readiness probes."""

    def filter(self, record: logging.LogRecord) -> bool:
        message = record.getMessage()
        return ('"GET /healthz ' not in message and '"GET /readyz ' not in message)


def _install_heartbeat_log_filter() -> None:
    access_logger = logging.getLogger("uvicorn.access")
    for existing in access_logger.filters:
        if isinstance(existing, _HeartbeatAccessLogFilter):
            return
    access_logger.addFilter(_HeartbeatAccessLogFilter())


def _build_mock_segment_bytes() -> bytes:
    if not FFMPEG_BIN:
        raise RuntimeError("ffmpeg is required to run the mock streaming server.")

    command = [
        FFMPEG_BIN,
        "-hide_banner",
        "-loglevel",
        "error",
        "-f",
        "lavfi",
        "-i",
        f"testsrc2=size={MOCK_FRAME_WIDTH}x{MOCK_FRAME_HEIGHT}:rate={MOCK_FPS}",
        "-t",
        f"{MOCK_DURATION_SECONDS}",
        "-an",
        "-c:v",
        "libx264",
        "-preset",
        "ultrafast",
        "-tune",
        "zerolatency",
        "-pix_fmt",
        "yuv420p",
        "-profile:v",
        "baseline",
        "-level",
        "3.0",
        "-movflags",
        "+empty_moov+default_base_moof+frag_keyframe",
        "-frag_duration",
        "250000",
        "-f",
        "mp4",
        "pipe:1",
    ]
    result = subprocess.run(
        command,
        check=True,
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
    )
    if not result.stdout:
        raise RuntimeError("Mock ffmpeg command did not return MP4 bytes.")
    return result.stdout


class PromptSubmission:

    def __init__(self, prompt_id: str, raw_prompt: str):
        self.prompt_id = prompt_id
        self.raw_prompt = raw_prompt


class ReadyPrompt:

    def __init__(
        self,
        *,
        prompt: str,
        source: str,
        prompt_id: str | None = None,
        seed_prompt_index: int | None = None,
        loop_iteration: int | None = None,
    ):
        self.prompt = prompt
        self.source = source
        self.prompt_id = prompt_id
        self.seed_prompt_index = seed_prompt_index
        self.loop_iteration = loop_iteration


@asynccontextmanager
async def lifespan(app: FastAPI):
    global MOCK_SEGMENT_BYTES
    del app
    print(f"Mock server starting with {LATENCY_MS}ms simulated latency...", flush=True)
    MOCK_SEGMENT_BYTES = _build_mock_segment_bytes()
    print(
        "Mock server ready "
        f"({len(MOCK_SEGMENT_BYTES) / (1024 * 1024):.2f} MiB fMP4 clip)",
        flush=True,
    )
    yield
    MOCK_SEGMENT_BYTES = None
    print("Mock server shutting down...", flush=True)


app = FastAPI(lifespan=lifespan)

app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)


